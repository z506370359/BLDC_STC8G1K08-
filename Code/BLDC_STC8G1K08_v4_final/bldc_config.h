/*******************************************************************************
 * bldc_config.h - 无刷直流风扇电机 配置头文件
 *
 * 目标芯片:  STC8G1K08-38I-TSSOP20, 24MHz内部IRC
 * 电机型号:  D8025 12V 5000RPM, 单霍尔, 两步换相
 *
 * 移植自: PIC16F1503 Fan-Single-HallSensor 项目 (本能电机 / Mr Zhong)
 ******************************************************************************/
#ifndef __BLDC_CONFIG_H__
#define __BLDC_CONFIG_H__

#include "stc8g.h"

/*=============================================================================
 * 系统时钟
 *===========================================================================*/
#define FOSC            24000000UL      /* 24MHz 内部高精度IRC */

/*=============================================================================
 * 引脚分配 (TSSOP20)
 *
 *  引脚  端口    功能说明
 *  ----  ------  --------------------------------
 *   1    P1.2    GPIO输出 -> HU (U相上桥臂MOS栅极)
 *   2    P1.3    GPIO输出 -> HV (V相上桥臂MOS栅极)
 *   3    P1.4    ADC4输入    调速指令 (PWM经RC滤波后的模拟电压)
 *   4    P1.5    GPIO输出    FG/RD 转速/故障反馈输出
 *   5    P1.6    预留        (HW W相上桥, 扩展三相时使用)
 *   6    P1.7    预留        (LW W相下桥, 扩展三相时使用)
 *   7    P5.4    复位/空     RST引脚
 *   8    VCC     电源
 *   9    P5.5    空
 *  10    GND     地
 *  11    P3.0    空          (可用INT4)
 *  12    P3.1    串口TX      (调试用, 可选)
 *  13    P3.2    INT0输入    霍尔传感器A (双沿中断)
 *  14    P3.3    空
 *  15    P3.4    CMPO输出    比较器输出 (调试用, 可选)
 *  16    P3.5    空
 *  17    P3.6    CMP-输入    过流保护基准电压 (外部分压)
 *  18    P3.7    CMP+输入    电流检测信号 (采样电阻电压)
 *  19    P1.0    CCP1输出 -> LV (V相下桥臂MOS栅极, PCA PWM)
 *  20    P1.1    CCP0输出 -> LU (U相下桥臂MOS栅极, PCA PWM)
 *===========================================================================*/

/* 上桥臂MOS栅极引脚 (GPIO直接驱动) */
#define PIN_HU          P12             /* U相上管 */
#define PIN_HV          P13             /* V相上管 */

/* 下桥臂MOS栅极引脚 (PCA PWM驱动) */
/* LU = P1.1 (CCP0), LV = P1.0 (CCP1) */
#define PIN_LU          P11
#define PIN_LV          P10

/* 霍尔传感器 */
#define PIN_HALL_A      P32             /* INT0输入 */

/* 反馈输出 */
#define PIN_FG_RD       P15             /* FG/RD 输出 */

/* 调速指令ADC通道 */
#define ADC_SPEED_CH    4               /* P1.4 = ADC通道4 */

/*=============================================================================
 * 电机参数
 *===========================================================================*/
#define POLES               2           /* 极对数 */
#define MAX_SPEED           5000UL      /* 最大转速 (RPM) */

/*=============================================================================
 * 软启动参数
 *===========================================================================*/
#define SOFT_START_START_DUTY   20      /* 起始占空比 (%) */
#define SOFT_START_END_DUTY    30       /* 结束占空比 (%) */
#define SOFT_START_DELAY       1500     /* 软启动持续时间 (毫秒) */

/*=============================================================================
 * PWM 配置
 *
 * PCA 8位PWM, 时钟源 FOSC/4:
 *   PWM频率 = 24MHz / 4 / 256 = 23437.5 Hz ≈ 23.4kHz
 *   占空比范围: 0 (关闭) ~ 255 (全开)
 *===========================================================================*/
#define PWM_DUTY_MAX    255
#define PWM_DUTY_MIN    0

/* PCA时钟源: FOSC/4 (CPS=101) */
#define PCA_CLOCK_CPS   0x0A            /* CMOD寄存器: CPS[2:0]=101, ECF=0 */

/*=============================================================================
 * 定时参数
 *===========================================================================*/
/* 上升沿延迟(超前角), 单位:定时器1计数值 (0.5μs/计数) */
#define RISING_DELAY        400         /* 200μs = 400 × 0.5μs */

/* 换相前软切换延迟 */
#define SOFT_SWITCH_DELAY   1000        /* 500μs = 1000 × 0.5μs */

/* 停止→启动 占空比门限 (输入低于等于此值认为关机)
 * 注意: 此值必须>=1, 否则 u8 <= 0 等价于 u8 == 0
 *       设为3与主循环全局停机条件(<=3%)保持一致 */
#define FAN_OFF_TO_START_DUTY  3

/* 堵转后重启延迟 */
#define RESTART_DELAY       5000        /* 毫秒 */

/*=============================================================================
 * PID (实际为PI) 控制参数
 *===========================================================================*/
#define CLOSE_LOOP                      /* 定义此宏启用闭环控制 */

#define PID_CYCLE           50          /* PI控制周期 (毫秒) */
#define OPEN_LOOP_CYCLE     150         /* 开环控制更新周期 (毫秒) */

/* PI增益 - 已适配8位占空比范围
 * 内部计算直接使用u16, 最大值255, 不会溢出 */
#define PWM_P_PARAMETER     12          /* 比例增益 */
#define PWM_I_PARAMETER     3           /* 积分增益 */

/*=============================================================================
 * FAN_BACK_OUT - 反馈线输出模式
 *   0=FG(转速脉冲), 1=RD(故障指示), 2=RDb(反向故障)
 *   3=LD(锁定), 4=LDb(反向锁定), 5=FGL, 6=FGH
 *===========================================================================*/
#define FAN_BACK_OUT        1           /* RD模式 */

#if   (FAN_BACK_OUT == 0)
  #define FAN_BACK_FG
  #define SetFgRdLdHigh()   (PIN_FG_RD = 1)
  #define SetFgRdLdLow()    (PIN_FG_RD = 0)
#elif (FAN_BACK_OUT == 1)
  #define FAN_BACK_RD
  #define SetFgRdLdHigh()   (PIN_FG_RD = 0)    /* RD模式反向: 故障时拉低 */
  #define SetFgRdLdLow()    (PIN_FG_RD = 1)    /* 正常时拉高 */
#elif (FAN_BACK_OUT == 2)
  #define FAN_BACK_RD
  #define SetFgRdLdHigh()   (PIN_FG_RD = 1)
  #define SetFgRdLdLow()    (PIN_FG_RD = 0)
#elif (FAN_BACK_OUT == 3)
  #define FAN_BACK_LD
  #define FAN_BACK_RD
  #define SetFgRdLdHigh()   (PIN_FG_RD = 1)
  #define SetFgRdLdLow()    (PIN_FG_RD = 0)
#elif (FAN_BACK_OUT == 4)
  #define FAN_BACK_LD
  #define FAN_BACK_RD
  #define SetFgRdLdHigh()   (PIN_FG_RD = 0)
  #define SetFgRdLdLow()    (PIN_FG_RD = 1)
#endif

/*=============================================================================
 * 转速计算常量
 *
 * 定时器1运行在2MHz(12T模式), 每个计数0.5μs
 * GetDoubleElectricalCyclePeriod() 返回4个半周期计数值之和
 * 转速RPM = 120,000,000 / 周期计数值总和
 *===========================================================================*/
#define SPEED_CALC_CONST    120000000UL

/*=============================================================================
 * 通用宏定义
 *===========================================================================*/
/* 关闭全部MOS管 */
#define AllMosOff()  do {       \
    CCAPM0 = 0x00;              \
    CCAPM1 = 0x00;              \
    PIN_LU = 0;                 \
    PIN_LV = 0;                 \
    PIN_HU = 0;                 \
    PIN_HV = 0;                 \
} while(0)

/* PCA通道PWM输出使能: ECOM=1, PWM=1 → 0x42 */
#define PCA_PWM_ON      0x42
/* PCA通道关闭 */
#define PCA_PWM_OFF     0x00

/* 百分比转8位占空比值 */
#define PercentToDuty(pct)  ((u8)((u16)(pct) * 255 / 100))

#endif /* __BLDC_CONFIG_H__ */
