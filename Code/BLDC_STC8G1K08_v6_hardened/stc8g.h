/*******************************************************************************
 * 文件名:    stc8g.h
 * 功能:      STC8G1K08 单片机特殊功能寄存器(SFR)定义 —— BLDC项目精简版
 *
 * 目标芯片:  STC8G1K08-38I-TSSOP20
 * 编译器:    Keil C51
 *
 * 说明: 本文件仅包含本BLDC风扇控制项目实际用到的寄存器定义, 完整的SFR列表
 *       请参考STC官方头文件(可通过STC-ISP工具下载)
 *
 * 版本历史:
 *   v1.0  - 首次发布
 *   v5.0  - 硬件匹配版 (适配P-MOS上管+N-MOS下管互补半桥)
 ******************************************************************************/
#ifndef __STC8G_H__                         /* 头文件保护宏: 防止重复包含 */
#define __STC8G_H__

#include <reg51.h>                          /* Keil C51 标准8051寄存器定义 */
#include <intrins.h>                        /* 内建函数: _nop_(), _crol_()等 */

/*=============================================================================
 *  端口寄存器
 *  STC8G每个GPIO由4个寄存器控制: Pn(数据), PnM0/PnM1(模式), PnPU(上拉)
 *  模式组合: M1:M0 = 00准双向口, 01推挽输出, 10高阻输入, 11开漏输出
 *===========================================================================*/
sfr P1      = 0x90;                         /* P1端口数据寄存器 */
sfr P1M0    = 0x92;                         /* P1端口模式0寄存器 */
sfr P1M1    = 0x91;                         /* P1端口模式1寄存器 */
sfr P3      = 0xB0;                         /* P3端口数据寄存器 */
sfr P3M0    = 0xB2;                         /* P3端口模式0寄存器 */
sfr P3M1    = 0xB1;                         /* P3端口模式1寄存器 */
sfr P5      = 0xC8;                         /* P5端口数据寄存器(仅P5.4/P5.5有效) */
sfr P5M0    = 0xCA;                         /* P5端口模式0寄存器 */
sfr P5M1    = 0xC9;                         /* P5端口模式1寄存器 */

/*=============================================================================
 *  端口位定义 —— 用于单个GPIO位操作
 *===========================================================================*/
sbit P10    = P1^0;                         /* P1.0 - CCP1/LV (V相下桥PWM) */
sbit P11    = P1^1;                         /* P1.1 - CCP0/LU (U相下桥PWM) */
sbit P12    = P1^2;                         /* P1.2 - HU (U相上桥驱动) */
sbit P13    = P1^3;                         /* P1.3 - HV (V相上桥驱动) */
sbit P14    = P1^4;                         /* P1.4 - ADC4 (调速输入) */
sbit P15    = P1^5;                         /* P1.5 - FG/RD (反馈输出) */
sbit P16    = P1^6;                         /* P1.6 - 预留 */
sbit P17    = P1^7;                         /* P1.7 - 预留 */

sbit P30    = P3^0;                         /* P3.0 - RXD/ISP下载 */
sbit P31    = P3^1;                         /* P3.1 - TXD/ISP下载 */
sbit P32    = P3^2;                         /* P3.2 - INT0/霍尔输入 */
sbit P33    = P3^3;                         /* P3.3 - 空 */
sbit P34    = P3^4;                         /* P3.4 - CMPO比较器输出 */
sbit P35    = P3^5;                         /* P3.5 - 空 */
sbit P36    = P3^6;                         /* P3.6 - CMP-过流基准输入 */
sbit P37    = P3^7;                         /* P3.7 - CMP+电流检测输入 */

sbit P54    = P5^4;                         /* P5.4 - RST复位引脚 */
sbit P55    = P5^5;                         /* P5.5 - 通用IO */

/*=============================================================================
 *  定时器辅助寄存器 AUXR
 *  用于配置定时器速度模式(1T/12T)等
 *===========================================================================*/
sfr AUXR    = 0x8E;
/* AUXR 各位功能:
 *   bit7: T0x12  (1=定时器0用1T模式, 0=12T模式)
 *   bit6: T1x12  (1=定时器1用1T模式, 0=12T模式)
 *   bit5: UART_M0x6  (UART0模式0波特率倍增)
 *   bit4: T2R    (定时器2运行控制)
 *   bit3: T2_C/T (定时器2时钟源: 0=定时器, 1=计数器)
 *   bit2: T2x12  (1=定时器2用1T模式)
 *   bit1: EXTRAM (0=允许访问xdata)
 *   bit0: S1ST2  (1=串口1用定时器2做波特率发生器)
 */

sfr INTCLKO = 0x8F;                         /* 中断与时钟输出寄存器 */
sfr T2H     = 0xD6;                         /* 定时器2高字节 */
sfr T2L     = 0xD7;                         /* 定时器2低字节 */

/*=============================================================================
 *  中断使能扩展寄存器 IE2
 *===========================================================================*/
sfr IE2     = 0xAF;
/* IE2 各位功能:
 *   bit2: ET2   (定时器2中断使能)
 *   bit1: ESPI  (SPI中断使能)
 *   bit0: ES2   (串口2中断使能)
 */

/*=============================================================================
 *  PCA/CCP/PWM 模块寄存器
 *  PCA是STC8G的核心外设之一, 本项目用它产生下桥臂PWM
 *===========================================================================*/
sfr CCON    = 0xD8;                         /* PCA控制寄存器 */
sbit CCF0   = CCON^0;                       /* CCP0 中断标志 (软件清零) */
sbit CCF1   = CCON^1;                       /* CCP1 中断标志 (软件清零) */
sbit CCF2   = CCON^2;                       /* CCP2 中断标志 (软件清零) */
sbit CR     = CCON^6;                       /* PCA计数器运行控制位 (1=启动) */
sbit CF     = CCON^7;                       /* PCA计数器溢出标志 (软件清零) */

sfr CMOD    = 0xD9;                         /* PCA工作模式寄存器 */
/* CMOD 各位功能:
 *   bit7: CIDL         (空闲模式下是否停止PCA, 0=继续, 1=停止)
 *   bit[4:1]: CPS[2:0] PCA时钟源选择:
 *     000=Fosc/12, 001=Fosc/2, 010=定时器0溢出,
 *     011=ECI外部脉冲, 100=Fosc, 101=Fosc/4, 110=Fosc/6, 111=Fosc/8
 *   bit0: ECF          (使能PCA溢出中断)
 */

sfr CL      = 0xE9;                         /* PCA 计数器低8位 */
sfr CH      = 0xF9;                         /* PCA 计数器高8位 */

sfr CCAPM0  = 0xDA;                         /* CCP0 工作模式寄存器 */
sfr CCAPM1  = 0xDB;                         /* CCP1 工作模式寄存器 */
sfr CCAPM2  = 0xDC;                         /* CCP2 工作模式寄存器 */
/* CCAPMn 各位功能:
 *   bit6: ECOM  (使能比较功能)
 *   bit5: CAPP  (上升沿捕获使能)
 *   bit4: CAPN  (下降沿捕获使能)
 *   bit3: MAT   (匹配中断使能)
 *   bit2: TOG   (匹配时引脚翻转)
 *   bit1: PWM   (PWM模式使能)
 *   bit0: ECCF  (使能CCFn中断)
 *   典型值: 0x42 = ECOM+PWM, 用于8位PWM输出
 */

sfr CCAP0L  = 0xEA;                         /* CCP0 捕获/比较 低字节 */
sfr CCAP0H  = 0xFA;                         /* CCP0 捕获/比较 高字节(PWM比较值) */
sfr CCAP1L  = 0xEB;                         /* CCP1 捕获/比较 低字节 */
sfr CCAP1H  = 0xFB;                         /* CCP1 捕获/比较 高字节(PWM比较值) */
sfr CCAP2L  = 0xEC;                         /* CCP2 捕获/比较 低字节 */
sfr CCAP2H  = 0xFC;                         /* CCP2 捕获/比较 高字节 */

sfr PCA_PWM0 = 0xF2;                        /* CCP0 PWM扩展位(用于10位PWM) */
sfr PCA_PWM1 = 0xF3;                        /* CCP1 PWM扩展位 */
sfr PCA_PWM2 = 0xF4;                        /* CCP2 PWM扩展位 */

/*=============================================================================
 *  ADC 模数转换寄存器
 *  本项目用ADC4(P1.4)采样调速输入信号
 *===========================================================================*/
sfr ADC_CONTR = 0xBC;                       /* ADC控制寄存器 */
/* ADC_CONTR 各位功能:
 *   bit7: ADC_POWER  (ADC电源开关, 1=开)
 *   bit6: ADC_START  (启动一次转换, 转换完成后自动清零)
 *   bit5: ADC_FLAG   (转换完成标志, 软件清零)
 *   bit[3:0]: ADC_CHS (通道选择 0~14)
 */
sfr ADC_RES   = 0xBD;                       /* 转换结果高字节 */
sfr ADC_RESL  = 0xBE;                       /* 转换结果低字节 */
sfr ADCCFG    = 0xDE;                       /* ADC配置寄存器 */
/* ADCCFG 各位功能:
 *   bit[5:4]: SPEED[1:0] (转换速度, 00最慢 11最快)
 *   bit0: RESFMT (结果对齐: 0=左对齐, 1=右对齐)
 */

/*=============================================================================
 *  比较器寄存器
 *  本项目用比较器做过流保护: CMP+接电流采样信号, CMP-接分压基准
 *===========================================================================*/
sfr CMPCR1  = 0xE6;                         /* 比较器控制1 */
/* CMPCR1 各位功能:
 *   bit7: CMPEN   (比较器使能)
 *   bit6: CMPIF   (中断标志, 软件写0清除)
 *   bit5: PIE     (正向边沿中断使能, 结果由0变1时触发)
 *   bit4: NIE     (负向边沿中断使能, 结果由1变0时触发)
 *   bit3: PIS     (正端输入选择: 0=P3.7/CMP+, 1=ADC通道)
 *   bit2: NIS     (负端输入选择: 0=内部1.19V基准, 1=P3.6/CMP-)
 *   bit1: CMPOE   (结果输出到P3.4/CMPO引脚使能)
 *   bit0: CMPRES  (当前比较结果, 只读, 1=CMP+>CMP-)
 */
sfr CMPCR2  = 0xE7;                         /* 比较器控制2 */
/* CMPCR2 各位功能:
 *   bit7: INVCMPO  (反转CMPO输出极性)
 *   bit6: DISFLT   (禁用数字滤波, 0=启用)
 *   bit[5:0]: LCDTY (数字滤波时钟周期数 1~63)
 */

/*=============================================================================
 *  外设引脚切换寄存器
 *===========================================================================*/
sfr P_SW2   = 0xBA;                         /* 外设功能切换寄存器2 */
/* P_SW2 bit7: EAXFR (扩展SFR区域访问使能, 必须置1才能访问部分寄存器) */

/*=============================================================================
 *  中断优先级寄存器
 *===========================================================================*/
sfr IP      = 0xB8;                         /* 中断优先级低位 */
sfr IPH     = 0xB7;                         /* 中断优先级高位 */
sfr IP2     = 0xB5;                         /* 中断优先级2低位 */
sfr IP2H    = 0xB6;                         /* 中断优先级2高位 */

/*=============================================================================
 *  看门狗寄存器
 *===========================================================================*/
sfr WDT_CONTR = 0xC1;                       /* 看门狗控制寄存器 */

/*=============================================================================
 *  中断向量号 —— Keil C51 interrupt 关键字使用
 *  每个向量对应一个 "interrupt N" 函数
 *===========================================================================*/
#define INT0_VECTOR      0                  /* 外部中断0 (霍尔A换相) */
#define TIMER0_VECTOR    1                  /* 定时器0中断 (1ms节拍) */
#define INT1_VECTOR      2                  /* 外部中断1 */
#define TIMER1_VECTOR    3                  /* 定时器1中断 (不使用) */
#define UART1_VECTOR     4                  /* 串口1中断 */
#define ADC_VECTOR       5                  /* ADC转换完成中断 */
#define LVD_VECTOR       6                  /* 低电压检测中断 */
#define PCA_VECTOR       7                  /* PCA模块中断 (超前角/软切换) */
#define UART2_VECTOR     8                  /* 串口2中断 */
#define SPI_VECTOR       9                  /* SPI中断 */
#define INT2_VECTOR     10                  /* 外部中断2 */
#define INT3_VECTOR     11                  /* 外部中断3 */
#define TIMER2_VECTOR   12                  /* 定时器2中断 */
#define CMP_VECTOR      21                  /* 比较器中断 (过流保护) */

/*=============================================================================
 *  常用数据类型简写 —— 提高代码可读性
 *===========================================================================*/
typedef unsigned char  u8;                  /* 无符号8位: 0~255 */
typedef unsigned int   u16;                 /* 无符号16位: 0~65535 */
typedef unsigned long  u32;                 /* 无符号32位: 0~4294967295 */
typedef signed char    s8;                  /* 有符号8位: -128~127 */
typedef signed int     s16;                 /* 有符号16位: -32768~32767 */
typedef signed long    s32;                 /* 有符号32位 */

#endif /* __STC8G_H__ */
