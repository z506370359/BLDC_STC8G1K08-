/*-----------------------------------------------------------------------------
 *  函数名:    ReadTimer1
 *  功能:      安全读取16位定时器1的当前计数值 (双读校验法)
 *  输入:      无
 *  输出:      u16 - 定时器1当前计数值 (0~65535, 每计数0.5μs)
 *
 *  原理:      8051读16位寄存器需要两条独立指令(先读高字节再读低字节),
 *             如果两次读取之间恰好发生低字节溢出(比如 0x0FFF → 0x1000),
 *             会读出 0x0F00 这样的错误值(低字节归零但高字节还未进位)
 *
 *  解决方法:  读完后再读一次高字节, 如果与第一次不一致说明发生了进位,
 *             重新读取直到两次高字节一致, 保证读出的是一致的16位值
 *
 *  备注:      此函数对换相时序精度至关重要, 若不加校验可能导致换相抖动
 *---------------------------------------------------------------------------*/
u16 ReadTimer1(void)
{
    u8 th, tl;                              /* 高字节和低字节 */
    do {
        th = TH1;                           /* 第一次读取高字节 */
        tl = TL1;                           /* 读取低字节 */
    } while (th != TH1);                    /* 再读一次高字节, 若变了则重来 */
    return ((u16)th << 8) | tl;             /* 拼接成16位值返回 */
}

