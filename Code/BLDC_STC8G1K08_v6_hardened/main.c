/*-----------------------------------------------------------------------------
 *  函数名:    InitGPIO
 *  功能:      初始化所有GPIO端口的模式和初始电平
 *  输入:      无
 *  输出:      无
 *
 *  端口模式说明 (PnM1:PnM0):
 *    00 = 准双向口 (带弱上拉, 兼容传统8051)
 *    01 = 推挽输出 (强驱动, 可输出大电流)
 *    10 = 高阻输入 (ADC/比较器输入必须用此模式)
 *    11 = 开漏输出 (需外部上拉)
 *
 *  备注:      P3.2霍尔输入必须用准双向口(00)模式, 利用内部弱上拉
 *             兼容开漏输出的霍尔IC(如MH182)。外部有上拉电阻时可改为高阻
 *---------------------------------------------------------------------------*/
void InitGPIO(void)
{
    /* 使能扩展SFR访问 (访问部分特殊寄存器需要) */
    P_SW2 |= 0x80;

    /*--- P1口配置 ---
     * P1.0 (CCP1/LV下管):  推挽  01
     * P1.1 (CCP0/LU下管):  推挽  01
     * P1.2 (HU上管):       推挽  01
     * P1.3 (HV上管):       推挽  01
     * P1.4 (ADC输入):      高阻  10  <-- 唯一的高阻口
     * P1.5 (FG/RD反馈):    推挽  01
     * P1.6 (预留):         推挽  01
     * P1.7 (预留):         推挽  01
     */
    P1M0 = 0xEF;                    /* 1110 1111: 除P1.4其余全推挽 */
    P1M1 = 0x10;                    /* 0001 0000: P1.4设为高阻(ADC输入) */
    P1   = 0x00;                    /* 全部输出低电平(MOS默认关断) */

    /*--- P3口配置 ---
     * P3.2 (INT0/霍尔):    准双向 00 <-- 内部弱上拉兼容开漏霍尔
     * P3.3:                推挽  01
     * P3.4 (CMPO):         推挽  01
     * P3.6 (CMP-基准):     高阻  10
     * P3.7 (CMP+检测):     高阻  10
     * 其余:                准双向 00
     */
    P3M0 = 0x18;                    /* 0001 1000: P3.3和P3.4推挽 */
    P3M1 = 0xC0;                    /* 1100 0000: P3.6和P3.7高阻 */
    P3   = 0x04;                    /* P3.2预置高电平, 配合内部弱上拉 */

    /*--- P5口: 只有P5.4(RST)和P5.5两个引脚 ---*/
    P5M0 = 0x00;
    P5M1 = 0x30;                    /* P5.4和P5.5设为高阻 */
}

/*-----------------------------------------------------------------------------
 *  函数名:    InitPCA
 *  功能:      初始化PCA模块, 配置23.4kHz的8位PWM输出
 *  输入:      无
 *  输出:      无
 *
 *  PCA配置:
 *    时钟源: FOSC/4 = 24MHz/4 = 6MHz
 *    PWM频率: 6MHz/256 = 23437.5Hz ≈ 23.4kHz
 *    两个通道: CCP0(P1.1=LU) 和 CCP1(P1.0=LV)
 *
 *  重要: STC8G的PCA PWM极性是反的!
 *    CL < CCAPnL → 输出低电平
 *    CL >= CCAPnL → 输出高电平
 *    所以 CCAPnH=0xFF 表示0%占空比, CCAPnH=0x00 表示100%占空比
 *    初始化时把CCAPnH设为0xFF确保MOS管关断
 *
 *  备注:      CR=0期间修改寄存器, 最后CR=1启动计数器, 避免毛刺
 *---------------------------------------------------------------------------*/
void InitPCA(void)
{
    CR = 0;                         /* 停止PCA计数器, 配置期间不工作 */
    CL = 0;                         /* 清零PCA计数器低8位 */
    CH = 0;                         /* 清零PCA计数器高8位 */

    /*--- CMOD寄存器: 0000 1011
     *    bit7=0 (空闲模式下继续计数, 非必需)
     *    bit[4:1]=0101 (CPS=101: 时钟源=FOSC/4)
     *    bit0=1 (ECF=1: 使能PCA溢出中断) */
    CMOD = 0x0B;

    /*--- CCP0 (P1.1 = LU下管) 初始化 ---*/
    CCAPM0   = PCA_PWM_OFF;         /* 先关闭通道, CCAP0M=0则引脚是GPIO */
    CCAP0H   = 0xFF;                /* 硬件反转值: 0xFF = 0%占空比 */
    CCAP0L   = 0xFF;                /* 比较值也设为0xFF, 保证一致性 */
    PCA_PWM0 = 0x00;                /* PWM扩展位清零(用8位PWM模式) */

    /*--- CCP1 (P1.0 = LV下管) 初始化 ---*/
    CCAPM1   = PCA_PWM_OFF;         /* 关闭通道 */
    CCAP1H   = 0xFF;                /* 0%占空比 */
    CCAP1L   = 0xFF;
    PCA_PWM1 = 0x00;

    /*--- CCP2: 本项目不使用 ---*/
    CCAPM2   = 0x00;

    pcaDutyCache = 0xFF;            /* 占空比缓存初始化为"0%"的硬件值 */

    CR = 1;                         /* 启动PCA计数器 */
}

/*-----------------------------------------------------------------------------
 *  函数名:    InitTimer0
 *  功能:      初始化定时器0, 产生1ms精确节拍中断
 *  输入:      无
 *  输出:      无
 *
 *  配置参数:
 *    模式: 16位自动重装(模式0)
 *    时钟: 1T模式, 计数频率 = FOSC = 24MHz
 *    重装值: 65536 - 24000 = 41536 = 0xA240
 *    中断频率: 1000Hz (每1ms一次)
 *
 *  备注:      这是整个系统的心跳, 所有状态机和定时操作都依赖它
 *---------------------------------------------------------------------------*/
void InitTimer0(void)
{
    AUXR |= 0x80;                   /* T0x12=1: 定时器0使用1T模式 */
    TMOD &= 0xF0;                   /* 清除T0的模式位, 默认为模式0 */
    TH0   = 0xA2;                   /* 重装值高字节 */
    TL0   = 0x40;                   /* 重装值低字节 */
    TF0   = 0;                      /* 清除溢出标志 */
    TR0   = 1;                      /* 启动定时器0 */
    ET0   = 1;                      /* 使能定时器0中断 */
}

/*-----------------------------------------------------------------------------
 *  函数名:    InitTimer1
 *  功能:      初始化定时器1作为自由运行计数器, 用于测量换相周期
 *  输入:      无
 *  输出:      无
 *
 *  配置参数:
 *    模式: 16位自动重装(模式0), 不开中断
 *    时钟: 12T模式, 计数频率 = FOSC/12 = 2MHz (每计数0.5μs)
 *    重装值: 0 (自由运行, 溢出周期 = 65536 × 0.5μs ≈ 32.768ms)
 *
 *  工作方式: 定时器1不触发中断, 由INT0换相中断手动读取其值并清零,
 *           如果定时器1溢出(TF1=1)说明超过32.7ms没换相, 判定为堵转
 *---------------------------------------------------------------------------*/
void InitTimer1(void)
{
    AUXR &= ~0x40;                  /* T1x12=0: 定时器1使用12T模式 */
    TMOD &= 0x0F;                   /* 清除T1的模式位, 默认为模式0 */
    TH1   = 0;                      /* 重装值 = 0 (从0开始计数) */
    TL1   = 0;
    TF1   = 0;                      /* 清除溢出标志 */
    TR1   = 1;                      /* 启动定时器1 */
    ET1   = 0;                      /* 不使能中断(我们在主程序中查询TF1) */
}

/*-----------------------------------------------------------------------------
 *  函数名:    InitADC
 *  功能:      初始化ADC模块, 配置为P1.4(ADC4)通道, 右对齐
 *  输入:      无
 *  输出:      无
 *
 *  配置参数:
 *    通道: ADC_SPEED_CH (=4, P1.4)
 *    分辨率: 10位
 *    对齐: 右对齐 (ADC_RES存高2位, ADC_RESL存低8位)
 *    速度: 01(适中, 约24个SYSCLK/转换)
 *
 *  注意:      转换结果读取方式必须严格匹配对齐格式,
 *             此项目历史版本曾因左右对齐搞混导致ADC值永远≤15的严重bug
 *---------------------------------------------------------------------------*/
void InitADC(void)
{
    ADCCFG    = 0x21;               /* 0010 0001: 速度=01, 右对齐 */
    ADC_CONTR = 0x80 | ADC_SPEED_CH; /* 1000 0100: 开电源, 选通道4 */

    adcAccVal    = 0;               /* 采样累加清零 */
    adcCnt       = 0;               /* 采样计数清零 */
    bAdcFirstRun = 1;               /* 标记首次运行, 丢弃第一次不准的结果 */

    ADC_CONTR |= 0x40;              /* 启动首次转换, 下个Timer0中断读取 */
}

/*-----------------------------------------------------------------------------
 *  函数名:    InitComparator
 *  功能:      初始化模拟比较器, 用于过流硬件保护
 *  输入:      无
 *  输出:      无
 *
 *  配置参数:
 *    CMP+ = P3.7 (接电流采样电阻电压, 经R20限流)
 *    CMP- = P3.6 (接外部分压基准 ≈ 0.455V)
 *    中断: 使能正向边沿 (CMP+由低变高时触发)
 *    滤波: 16个时钟周期的数字滤波, 抗干扰
 *
 *  工作原理: 当电机电流使采样电压>0.455V时, CMP输出变高,
 *             触发CMP_ISR中断, 立即关闭全部MOS管
 *
 *  备注:      与PIC的CWG硬件自动关断相比, STC为软件中断关断,
 *             响应延迟2~5μs, 对风扇场景足够
 *---------------------------------------------------------------------------*/
void InitComparator(void)
{
    CMPCR1 = 0x00;                  /* 先关闭比较器, 清除所有配置 */
    CMPCR2 = 0x10;                  /* 启用数字滤波, 16个时钟周期 */

    /*--- CMPCR1 = 1010 0100 ---
     *    bit7=1 (CMPEN: 使能比较器)
     *    bit6=0 (CMPIF: 清除中断标志)
     *    bit5=1 (PIE: 使能正向边沿中断)
     *    bit4=0 (NIE: 不使能负向边沿中断)
     *    bit3=0 (PIS: 正端用P3.7)
     *    bit2=1 (NIS: 负端用P3.6外部基准)
     *    bit1=0 (CMPOE: 不输出到P3.4, 如需调试可改为1)
     *    bit0=0 (CMPRES: 只读) */
    CMPCR1 = 0xA4;
}

/*-----------------------------------------------------------------------------
 *  函数名:    InitInterrupts
 *  功能:      初始化中断系统, 配置INT0触发方式和中断优先级
 *  输入:      无
 *  输出:      无
 *
 *  配置内容:
 *    INT0: 双沿触发 (IT0=0), 电机关闭时禁用, MotorOn()时启用
 *    优先级: INT0=最高, 其他默认
 *    EA: 开启全局中断
 *
 *  备注:      INT0优先级最高确保换相精度不被其他中断干扰
 *---------------------------------------------------------------------------*/
void InitInterrupts(void)
{
    /* 初始化换相周期历史记录为最大值, 表示"尚未测量" */
    phasePeriod[0] = 0xFFFF;
    phasePeriod[1] = 0xFFFF;
    phasePeriod[2] = 0xFFFF;
    phasePeriod[3] = 0xFFFF;
    electricalCyclePeriod = 0xFFFF;
    phaseChangeCnt = 0;

    /*--- INT0配置 ---
     *    IT0=0: 双沿触发 (STC8G专有, 标准8051只支持下降沿)
     *    EX0=0: 先禁用, MotorOn()时再开启 */
    IT0 = 0;
    EX0 = 0;

    /*--- 设置INT0为最高优先级 ---
     *    PX0=1, IPH的INT0位也置1, 优先级最高(11) */
    PX0 = 1;
    IP |= 0x01;

    EA = 1;                         /* 打开全局中断 */
}

/*-----------------------------------------------------------------------------
 *  函数名:    InitSystem
 *  功能:      系统总初始化, 依次调用各模块初始化
 *  输入:      无
 *  输出:      无
 *
 *  调用顺序很重要:
 *    1. 关看门狗, 防止初始化超时
 *    2. 关中断, 初始化期间不响应任何中断
 *    3. GPIO必须最先初始化, 确保引脚状态正确
 *    4. AllMosOff()确保MOS初始关断
 *    5. 再配置PCA/定时器/ADC/比较器
 *    6. 最后开中断
 *---------------------------------------------------------------------------*/
void InitSystem(void)
{
    WDT_CONTR = 0x00;               /* 初始化期间关闭看门狗 */
    EA = 0;                         /* 关闭总中断 */

    InitGPIO();                     /* GPIO端口模式 */
    AllMosOff();                    /* 确保所有MOS关断 */
    InitPCA();                      /* PCA PWM模块 */
    InitTimer0();                   /* 1ms节拍定时器 */
    InitTimer1();                   /* 换相计时器 */
    InitADC();                      /* 模数转换器 */
    InitComparator();               /* 过流保护比较器 */
    InitInterrupts();               /* 中断系统(最后开EA) */
}

