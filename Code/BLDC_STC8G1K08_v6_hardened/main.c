/*-----------------------------------------------------------------------------
 *  宏名:      ResetMsClk()
 *  功能:      原子操作清零毫秒计数器
 *  原理:      fanBaseMsClk是u16(16位), 8051写入需要两条指令,
 *             如果写入中途被定时器0中断打断, 会导致计数值错乱
 *             因此必须先关中断, 写入完成再开中断
 *  使用场景:  所有 fanBaseMsClk=0 的地方都应该用这个宏
 *---------------------------------------------------------------------------*/
#define ResetMsClk() do { EA=0; fanBaseMsClk=0; EA=1; } while(0)

/*-----------------------------------------------------------------------------
 *  函数名:    ReadMsClk
 *  功能:      原子读取毫秒计数器 (防止u16撕裂读)
 *  输入:      无
 *  输出:      u16 - fanBaseMsClk的一致性快照
 *
 *  原理:      与写入同理, 主循环读取u16时若被Timer0中断打断,
 *             可能读到"高字节旧值+低字节新值"的错位组合
 *             解决: 关中断→读取→开中断, 保证一致性
 *
 *  性能:      两条关/开中断指令 + 一次16位拷贝, 成本约10个时钟周期
 *             1T模式下 ~0.4μs, 可忽略不计
 *---------------------------------------------------------------------------*/
static u16 ReadMsClk(void)
{
    u16 tmp;
    EA = 0;                 /* 关总中断, 防止读取被Timer0打断 */
    tmp = fanBaseMsClk;     /* 原子性地读取16位值 */
    EA = 1;                 /* 恢复中断 */
    return tmp;
}

