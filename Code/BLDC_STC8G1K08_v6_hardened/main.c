/*-----------------------------------------------------------------------------
 *  函数名:    AdcProcess
 *  功能:      ADC采样处理 (在Timer0中断中每1ms调用一次, 非阻塞)
 *  输入:      无
 *  输出:      无 (通过全局变量输出: rawPwmInDuty, pwmInDuty)
 *
 *  工作方式 (异步非阻塞):
 *    每次进来先检查上次转换是否完成 → 未完成则直接返回
 *    完成则读取结果并立即启动下次转换, 结果在下次调用时读取
 *    这样避免了 while(!ADC_FLAG) 死等, CPU时间几乎零浪费
 *
 *  平均滤波:
 *    累加25次采样 (25ms) → 取平均 → 换算为百分比 → 取反
 *
 *  取反原因:
 *    与PIC原版保持一致, 硬件上分压器接法使得VIN高电平表示"慢",
 *    VIN低电平表示"快", 因此需要 duty = 100 - adc%
 *
 *  历史教训:
 *    v1版直接 while() 等待ADC完成, 每次1ms中断阻塞15μs
 *    v2版改为异步模式, 彻底消除阻塞
 *---------------------------------------------------------------------------*/
void AdcProcess(void)
{
    u16 adcVal;                     /* ADC本次读取值 */

    /*--- 检查上次转换是否已完成 ---*/
    if (!(ADC_CONTR & 0x20))        /* ADC_FLAG=0 表示还在转换中 */
        return;                     /* 直接返回, 不等待 */

    ADC_CONTR &= ~0x20;             /* 清除ADC_FLAG */

    /*--- 首次运行: 丢弃第一次结果(上电后第一次可能不准) ---*/
    if (bAdcFirstRun) {
        bAdcFirstRun = 0;           /* 标志清零 */
        ADC_CONTR |= 0x40;          /* 启动下次转换 */
        return;
    }

    /*--- 读取10位转换结果 (右对齐) ---
     *    ADC_RES 的低2位 = 结果高2位
     *    ADC_RESL 的全8位 = 结果低8位
     *    拼接: (RES & 0x03) << 8 | RESL
     *    历史教训: v1版错用左对齐代码导致值永远≤15 */
    adcVal = ((u16)(ADC_RES & 0x03) << 8) | ADC_RESL;

    /*--- 立即启动下次转换 (后台进行, 不阻塞) ---*/
    ADC_CONTR |= 0x40;

    /*--- 累加滤波 ---*/
    adcAccVal += adcVal;            /* 累加当前值 */

    if (++adcCnt >= ADC_SAMPLE_CNT) {/* 满25次则取平均 */
        adcCnt = 0;
        adcVal    = adcAccVal / ADC_SAMPLE_CNT;  /* 平均值 */
        adcAccVal = 0;                           /* 清零累加器 */

        adcVal = adcVal * 100 / 1023;           /* 0-1023 换算为 0-100 */
        if (adcVal > 100)                       /* 保护: 不超过100 */
            adcVal = 100;

        rawPwmInDuty = 100 - (u8)adcVal;        /* 取反: 与PIC原版一致 */

#ifdef CLOSE_LOOP
        pwmInDuty = targetSpeedList[rawPwmInDuty];  /* 闭环: 查表得目标转速 */
#else
        pwmInDuty = rawPwmInDuty;                   /* 开环: 直接使用 */
#endif
    }
}

