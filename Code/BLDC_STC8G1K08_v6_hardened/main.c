/*-----------------------------------------------------------------------------
 *  函数名:    WritePwmDuty
 *  功能:      设置PWM输出占空比 (自动处理硬件极性反转)
 *  输入:      duty_val - 逻辑占空比 (0=关断, 255=满占空比)
 *  输出:      无
 *
 *  关键原理 (STC8G PCA PWM极性反转):
 *    硬件行为: CL < CCAPnL → 输出低, CL >= CCAPnL → 输出高
 *    实际占空比 = (256 - CCAPnH) / 256
 *    即: CCAPnH=0 表示100%占空比, CCAPnH=255 表示约0%
 *
 *  本函数的做法:
 *    pwmOutDuty (逻辑值, 0~255) - 给PI控制器使用, 直观的占空比
 *    pcaDutyCache (硬件值, 255-逻辑值) - 直接写入CCAPnH寄存器
 *
 *  历史教训:
 *    v3版忘记极性反转导致软启动设20%实际输出80%, PI越加速实际占空比越小,
 *    形成正反馈必定堵转。v4修复后工作正常。
 *---------------------------------------------------------------------------*/
void WritePwmDuty(u8 duty_val)
{
    if (duty_val > PWM_DUTY_MAX)    /* 上限钳位: 防止超过最大值 */
        duty_val = PWM_DUTY_MAX;

    pwmOutDuty   = duty_val;        /* 保存逻辑占空比供PI使用 */
    pcaDutyCache = 255 - duty_val;  /* 反转后的硬件值, 待PCA中断写入 */
}

/*-----------------------------------------------------------------------------
 *  函数名:    ExecuteCommutation
 *  功能:      执行电机换相 (根据霍尔状态切换MOS导通组合)
 *  输入:      无 (读取全局 bHallState 和 pcaDutyCache)
 *  输出:      无
 *
 *  两步换相逻辑:
 *    霍尔A高: HU(上管)导通 + LV(下管)PWM → 电流 U相→V相
 *    霍尔A低: HV(上管)导通 + LU(下管)PWM → 电流 V相→U相
 *
 *  执行顺序:
 *    1. AllMosOff先全部关断 - 防止换相瞬间上下桥臂直通短路
 *    2. 把占空比缓存写入两个PCA通道寄存器
 *    3. 根据霍尔状态, 置位上管GPIO并使能对应下管PCA PWM输出
 *
 *  备注:
 *    - CCAPM寄存器清零时PCA引脚恢复为GPIO功能, 因为端口已预置为0, MOS自动关断
 *    - 上桥臂用GPIO直接控制 (NPN三极管驱动, 不需要PWM)
 *    - 下桥臂用PCA PWM控制 (调节占空比 = 调节转速)
 *---------------------------------------------------------------------------*/
void ExecuteCommutation(void)
{
    AllMosOff();                    /* 第1步: 先关闭全部MOS, 防止直通 */

    /*--- 第2步: 将占空比写入PCA通道 ---*/
    CCAP0H = pcaDutyCache;          /* CCP0 (LU) 占空比 */
    CCAP1H = pcaDutyCache;          /* CCP1 (LV) 占空比 */

    /*--- 第3步: 根据霍尔电平选择导通组合 ---*/
    if (bHallState) {
        /* 霍尔A=高: U相上管导通 + V相下管PWM */
        PIN_HU = 1;                 /* GPIO输出高, 驱动HU NPN→P-MOS导通 */
        CCAPM1 = PCA_PWM_ON;        /* 使能CCP1, P1.0输出PWM驱动N-MOS */
    } else {
        /* 霍尔A=低: V相上管导通 + U相下管PWM */
        PIN_HV = 1;                 /* GPIO输出高, 驱动HV NPN→P-MOS导通 */
        CCAPM0 = PCA_PWM_ON;        /* 使能CCP0, P1.1输出PWM驱动N-MOS */
    }
}

